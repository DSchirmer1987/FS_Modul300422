
public class Compare {

	public static void main(String[] args) {
		String a = "Anna";
		String b = "Bertha";
		String c = "Christa";
		
		System.out.println(a.compareTo(b));
		System.out.println(b.compareTo(a));
		
		System.out.println(a.compareTo(c));
		System.out.println(c.compareTo(a));
	}

}
